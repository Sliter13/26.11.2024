my_integer = 10
my_float = 5.5

float_to_int = int(my_float)
print(float_to_int)

int_to_float = float(my_integer)
print(int_to_float)

int_to_str = str(my_integer)
float_to_str = str(my_float)
print(int_to_str)
print(float_to_str)

int_to_bool = bool(my_integer)
float_to_bool = bool(my_float)

print(int_to_bool)
print(float_to_bool)